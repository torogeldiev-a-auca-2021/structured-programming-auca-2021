import java.util.Scanner;

public class URI1149 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int  x , y;
        while (true){
            x = sc.nextInt();
            if (x > 0)break;
            }
        while (true){
            y = sc.nextInt();
            if( y > 0)break;

        }
        int sum = 0;
        for(int i = x; i < x + y;i++){
            sum = x + i;
        }
        System.out.println(sum);

        }
            }

